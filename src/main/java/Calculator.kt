class Calculator() {

    fun add(num1: Int, num2: Int): Int {
        return (num1 + num2)
    }

    fun subtract(num1: Int, num2: Int): Int {
        return (num1 - num2)
    }

    fun multiply(num1: Int, num2: Int): Int {
        return (num1 * num2)
    }

    fun divide(num1: Double, num2: Double): Double {
        return (num1 / num2)
    }
}